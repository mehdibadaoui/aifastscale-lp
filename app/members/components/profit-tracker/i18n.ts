export function getTranslations(lang: string): TranslationKey {
  const supportedLang = lang.split('-')[0] as Language
  if (supportedLang in translations) {
    return translations[supportedLang] as unknown as TranslationKey
  }
  return translations.en
}

// Detect user's language
export function detectLanguage(): Language {
  if (typeof navigator === 'undefined') return 'en'
  const browserLang = navigator.language.split('-')[0]
  return (browserLang in translations ? browserLang : 'en') as Language
}

